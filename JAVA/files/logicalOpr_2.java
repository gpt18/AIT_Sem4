package files;
public class logicalOpr_2 {
    public static void main(String[] args) {
        System.out.println((6>4)&&(9>6));
        System.out.println((6>3)&&(9<6));
        System.out.println((6<3)||(8>5));
        System.out.println((6>3)||(8<5));
        System.out.println((6<4)||(8<6));
        System.out.println(!(6==4));
        System.out.println(!(6>4));
        
    }
}
